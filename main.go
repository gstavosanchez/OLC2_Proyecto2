package main

import (
	"fmt"
)

var P, H float64
var stack [30101999]float64
var heap [30101999]float64
var t0, t1, t2, t3, t4 float64

// -----------------------------------------------------------
// FUNCIONES
func sumar() {
	// -----------------------------------------------------------
	// ACCESO A VARIABLE: A
	t1 = P + 1 //Posicionar el puntero
	t0 = stack[int(t1)]
	// FIN ACCESO
	// -----------------------------------------------------------

	// PRIMITIVO: 50
	t2 = t0 * 50

	stack[int(P)] = t2
	goto L0
L0:
	return
}

// ===========================================================
// MAIN
// ===========================================================
func main() {
	P = 0 // Puntero Stack
	H = 0 //Puntero Heap

	// -----------------------------------------------------------
	// LLAMADO A FUNCION: SUMAR
	// PRIMITIVO: 5
	t3 = P + 1
	stack[int(t3)] = 5
	P = P + 0
	sumar()
	t3 = stack[int(P)]
	P = P - 0
	// FIN LLAMADO A FUNCION
	// -----------------------------------------------------------

	// -----------------------------------------------------------
	// ASIGNACION: A
	stack[int(0)] = t3
	// FIN ASIGNACION
	// -----------------------------------------------------------

	// -----------------------------------------------------------
	// INICIO PRINT
	// -----------------------------------------------------------
	// ACCESO A VARIABLE: A
	t4 = stack[int(0)]
	// FIN ACCESO
	// -----------------------------------------------------------

	fmt.Printf("%g", t4)
	fmt.Printf("%c", int(10))
	// FIN PRINT
	// -----------------------------------------------------------

}
